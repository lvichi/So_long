#include "so_long.h"

void	print_map(char **map)
{
	int	i;
	int	j;

	i = 0;
	while (i < 10)
	{
		j = 0;
		while (map[i] && map[i][j])
			write(1, &map[i][j++], 1);
		i++;
	}
	write(1, "\n", 1);
}
